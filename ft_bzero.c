#include <string.h>
#include <stdio.h>

int	main(void)
{
	char	arr[10] = "0123456789";
	int	i = 0;

	bzero(arr + 2, 4);
	while (i < 10)
	{
		printf("[%d]: %c\n", i, arr[i]);
		i++;
	}
	return (0);
}
